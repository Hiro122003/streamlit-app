import streamlit as st

# ページ設定（最初に呼び出す必要がある）
st.set_page_config(
    page_title="売上データ分析アプリ",
    layout="wide"
)

# アプリタイトルの表示
st.title("売上データ分析アプリ")

# アプリの説明文
st.markdown(
    """
    このアプリはCSV形式の売上データと会員データを読み込み、
    集計・可視化を行うダッシュボードです。

    左のサイドバーからページを選択して各種分析をご覧ください。
    """
)

# 使用データの概要
st.info(
    """
    **使用データ**
    - **Sales_Data.csv**：取引履歴データ（1,000件）。取引日時・会員ID・商品ID・店舗ID・数量・金額を含む。
    - **Member_Data.csv**：会員情報データ（300件）。性別・年齢・登録日数を含む。
    """
)

# サイドバーのナビゲーション説明
st.sidebar.markdown(
    """
    ## ナビゲーション
    以下のページから分析内容を選択してください。

    - **データ概要**：件数・カラム・型・欠損値の確認
    - **KPIサマリー**：売上・取引件数などの主要指標
    - **時系列分析**：月別・曜日別の売上推移
    - **店舗・商品分析**：売上Top10の確認
    - **会員分析**：性別・年代別の購買傾向
    """
)
